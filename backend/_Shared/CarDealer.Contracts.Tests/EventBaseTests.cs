using CarDealer.Contracts.Abstractions;
using FluentAssertions;

namespace CarDealer.Contracts.Tests;

// Concrete implementation for testing the abstract EventBase
public class TestEvent : EventBase
{
    public override string EventType => "test.event.created";
    public string TestData { get; set; } = string.Empty;
}

public class EventBaseTests
{
    [Fact]
    public void EventId_IsAutoGenerated()
    {
        var event1 = new TestEvent();
        var event2 = new TestEvent();

        event1.EventId.Should().NotBeEmpty();
        event2.EventId.Should().NotBeEmpty();
        event1.EventId.Should().NotBe(event2.EventId);
    }

    [Fact]
    public void OccurredAt_IsSetToUtcNow()
    {
        var before = DateTime.UtcNow.AddSeconds(-1);
        var evt = new TestEvent();
        var after = DateTime.UtcNow.AddSeconds(1);

        evt.OccurredAt.Should().BeAfter(before);
        evt.OccurredAt.Should().BeBefore(after);
    }

    [Fact]
    public void SchemaVersion_DefaultsToOne()
    {
        var evt = new TestEvent();
        evt.SchemaVersion.Should().Be(1);
    }

    [Fact]
    public void CorrelationId_DefaultsToNull()
    {
        var evt = new TestEvent();
        evt.CorrelationId.Should().BeNull();
    }

    [Fact]
    public void CorrelationId_CanBeSet()
    {
        var correlationId = Guid.NewGuid().ToString();
        var evt = new TestEvent { CorrelationId = correlationId };

        evt.CorrelationId.Should().Be(correlationId);
    }

    [Fact]
    public void EventType_ReturnsSubclassValue()
    {
        var evt = new TestEvent();
        evt.EventType.Should().Be("test.event.created");
    }

    [Fact]
    public void SchemaVersion_CanBeUpdated()
    {
        var evt = new TestEvent { SchemaVersion = 2 };
        evt.SchemaVersion.Should().Be(2);
    }

    [Fact]
    public void ImplementsIEvent()
    {
        var evt = new TestEvent();
        evt.Should().BeAssignableTo<IEvent>();
    }
}
